Crescendo is a development accelerator enabling you to rapidly build PowerShell cmdlets that leverage existing command-line tools. 
Crescendo amplifies the command-line experience of the original tool to include object output for the PowerShell pipeline, 
privilege elevation, and integrated help information. 
A Crescendo module replaces cumbersome command-line tools with PowerShell cmdlets that are easier to use in automation and packaged to share with team members.

